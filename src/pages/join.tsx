function fixCode(code: string) {
  let letters = code.split('');
  if(letters.length > 6) letters.splice(6, letters.length-6)
  for(let i = 0; i < letters.length; i++) {
    if(!allowedChars.includes(letters[i] as string)) letters.splice(i,1);
  }
  return letters.join('');
}

